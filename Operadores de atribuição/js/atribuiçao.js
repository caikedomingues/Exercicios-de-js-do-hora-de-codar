/*                                    operadores de atribuição
Temos algumas maneiras de atribuir um valor a uma variável;
As mais utilizadas são: +=, -=, *=, /=;
Basicamente é uma forma resumida de operação: x = x + y;
Em loops também é comum utilizar os operadores ++ ou --;

*/

/*Vamos ver um exemplo de atribuição */
//Soma
var x = 1 
var y = 2

console.log(x = x + y)
console.log(x += y)

//Subtração
console.log(x -= y)

//Multiplicação
console.log(x *= y)

//Divisão
console.log(x /= y)

