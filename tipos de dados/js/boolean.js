/*                              Boolean
representa um valor verdadeiro ou falso;
comparações resultam em booleans;
Podemos também atribuir um true (verdadeiro) ou false (falso) para
variáveis e ela terá o tipo de boolean.
 */

/*Para começarmos vamos criar uma variável com atribuição true 
(verdadeiro) */

var verdadeiro = true
console.log(true)
/*Vamos conferir o tipo */
console.log(typeof verdadeiro)

/*Agora vamos criar uma variável com atribuição false(falso) */

var falso = false
console.log(falso)
console.log(typeof falso)

